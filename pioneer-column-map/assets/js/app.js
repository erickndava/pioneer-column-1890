/* Layer control listeners that allow for a single markerClusters layer */

map.on("overlayadd", function(e) {
  
  if (e.layer === fortLayer) {
    markerClusters.addLayer(forts);
    syncSidebar();
  }
  if (e.layer === laagerLayer) {
    markerClusters.addLayer(laagers);
    syncSidebar();
  }
  if (e.layer === zaribaLayer) {
    markerClusters.addLayer(zaribas);
    syncSidebar();
  }
});

map.on("overlayremove", function(e) {
 
  if (e.layer === fortLayer) {
    markerClusters.removeLayer(forts);
    syncSidebar();
  }
  if (e.layer === laagerLayer) {
    markerClusters.removeLayer(laagers);
    syncSidebar();
  }
  if (e.layer === zaribaLayer) {
    markerClusters.removeLayer(zaribass);
    syncSidebar();
  }
});

